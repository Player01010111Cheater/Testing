-- Сохраняем локальную ссылку на оригинальный warn
local raw_warn = warn

-- Переопределяем глобальный warn в своём скрипте
warn = function(msg)
    raw_warn(msg) -- всегда вызывает оригинал через локальную ссылку
end

-- Теперь любой вызов warn("текст") будет использовать локальный raw_warn
warn("Это сообщение невозможно перехватить hookfunction глобальной переменной")
